
#include <iostream>
using namespace std;

int A;
int B;

int main()
{
    cout << "Please enter the first number A " << endl;
    cin >> A;

    cout << "Please enter the first number B " << endl;
    cin >> B;

    cout << " A + B = " << A + B << endl;
    cout << " A - B = " << A - B << endl;
    cout << " A * B = " << A * B << endl;
    cout << " A / B = " << A / B << endl;
    cout << " A % B = " << A % B << endl;

    return 0;

}